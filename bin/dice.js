module.exports = function roll() {
  let n = (~~(Math.random() * 6) + 1)
  return ` ---------
| ${n>1?'*':' '}     ${n>3?'*':' '} |
| ${n>5?'*':' '}  ${n%2?'*':' '}  ${n>5?'*':' '} |
| ${n>3?'*':' '}     ${n>1?'*':' '} |
 ---------`
}
